package Models;

public class Informacoes {

    private String ipMaquina;
    private String nomeSO;
    private float totalR;
    private String espacoDisco;
    private String processador;
    private String usoDisco;
    
    public String getIpMaquina() {
        return ipMaquina;
    }

    public void setIpMaquina(String ipMaquina) {
        this.ipMaquina = ipMaquina;
    }

    public String getNomeSO() {
        return nomeSO;
    }

    public void setNomeSO(String nomeSO) {
        this.nomeSO = nomeSO;
    }

    public float getTotalR() {
        return totalR;
    }

    public void setTotalR(float totalR) {
        this.totalR = totalR;
    }

    public String getEspacoDisco() {
        return espacoDisco;
    }

    public void setEspacoDisco(String espacoDisco) {
        this.espacoDisco = espacoDisco;
    }

    public String getProcessador() {
        return processador;
    }

    public void setProcessador(String processador) {
        this.processador = processador;
    }

    public String getUsoDisco() {
        return usoDisco;
    }

    public void setUsoDisco(String usoDisco) {
        this.usoDisco = usoDisco;
    }

}


